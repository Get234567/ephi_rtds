                                    <h2>Mass drug administration</h2><p>
                                    All communities in Wolaita will receive
                                    expanded MDA organised and overseen by the Federal
                                    Ministry of Health (FMoH) and implemented at
                                    community-level by the Health Extension Workers
                                    (HEWs). All community members ≥ 1 year of age will
                                    receive ALB against STH, while those ≥ 5 years of age
                                    will receive PZQ for schistosomiasis infections.<br><br>
                                    All communities, regardless of arm and prevalence,
will receive biannual ALB treatment, and annual PZQ
treatment, both with a target of 90% verified coverage,
with 10% confidence interval in all age groups. As well
as administering ALB and PZQ, HEWs will be trained
to collect digital data on individual compliance to treatment.
For each treatment record, individuals who present
for MDA will be identified using their fingerprint
information and, if their fingerprint is not identified, or
if they do not consent to their fingerprint being registered,
their barcoded study ID card will be used. If the
individual does not have an ID card (due to not being
reached by census or having lost it) they will have their
demographics recorded (e.g. age and sex), fingerprints
registered (if they consent) and be given a new study ID
card. The HEW will then record if the individual ingests
the medication (ALB and PZQ separately) and, if not,
their reasons for refusal. At the end of each round of
MDA, a list of people registered in census who were not
identified during MDA will be fed back to the HEWs for
MDA mop-up.
