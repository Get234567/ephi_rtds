                                    <h2>WaSH</h2><p>
                                    Water, sanitation and hygiene (WaSH)
In Arm 1, improved WaSH interventions targeting risk
behaviour and barriers to access will be implemented
through WVE. In Year 1, the aim is for 40% of the communities
to be declared open defecation free (ODF)
through CLTS, whereby communities work together
to build household level, institutional, and community
improved pit latrines. CLTS is an approach to addressing
open defecation that triggers emotions such as shame
and disgust to generate a collective demand for sanitation
within a community. In Ethiopia, evidence has shown
that HEW-facilitated CLTS can be effective and is currently
the strategy promoted by the national OneWASH
programme.<br><br> In addition, WaSH business centres
will be established to offer products for purchase (latrine
slabs, handwashing stations, soap and brooms) and services
(pit digging, super structure labour, and maintenance
of infrastructure) through sale and loans. Water
infrastructure will be provided to 70% of the population
through wells and taps. This will be scaled up to 85%
from Year 2. The goal for sanitation is the achievement of
82% basic improved latrines.

