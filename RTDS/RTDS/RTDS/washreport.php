                Executive Summary
      The Geshiyaro intervention impact will be measuring through annual, longitudinal parasitological sentinel site surveillance across approximately 15% of all kebeles. The data presented here are baseline data from 3,926 individuals sampled in 26 sentinel sites between January and March 2020. In each site prevalence and intensity of Schistosoma mansoni, Hookworm, Trichuris, Ascaris and Schistosoma haematobium were investigated within the community from pre-school aged children (SAC) until adults. The overall prevalence of Schistosoma mansoni was low (3.02% by Kato Katz; 14% by trace -ve POC-CCA; and 21% by trace +ve POC-CCA), with an arithmetic mean intensity of infection of 0.88 eggs per gram (epg). Furthermore, very few heavy infections (0.7%) were detected. Equally, the prevalence of Hookworm, Trichuris and Ascaris was low at 12.5%, 3.02% and 13.0%, with an arithmetic mean intensity of infection of 22.4 epg, 16.8%, and 224 epg, respectively. Heavy infections were only detected in hookworm at low levels (0.08%). This lack of heavy infections in both species is important and promising for transmission break since there are very few individuals harbouring heavy egg burdens to maintain transmission in a community.  This is also the first data on schistosomiasis and STH infection rates among adults in Ethiopia and demonstrates the importance of community level treatment.

