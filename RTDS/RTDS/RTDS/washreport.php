                Open Defecation Free (ODF) is defined primarily and most basically as the absence of the practice of open defecation (OD) in a prescribed community. Implicitly it means that all members of that community have access to and are using a latrine. Communities free of open defecation practices and with improved sanitation is a desired goal for the Geshiyaro project as the transmission of soil-transmitted helminths (STH) and schistosomiasis thrives in unsanitary conditions.
                <br>
                <b>The specific ODF verification survey objectives are:</b><br>
      •	Total elimination of open defecation practices.<br>
      •	100% coverage of latrine use.<br>
      •	Improved personal, household and environmental hygiene through education.<br>
      •	Increased ownership and sustainability of hygiene and sanitation activities through education.<br>
      •	Contribution to reduction in sanitation related diseases. <br><br>
      For the Geshiyaro project, ODF status had been declared and verified in 12 out of 32 kebeles (40%) in Bolosso Sore district at three levels - the woreda (district), kebele (community) by World Vision Ethiopia (WVE). An independent team from the Ethiopian Public Health Institute (EPHI), with support from the London Centre for Neglected Tropical Disease Research (LCNTDR), carried out evaluation of ODF verification in the 12 kebeles. Overall 30% of the total number of households in each kebele (a total of 3,548 households across all 12 kebeles) were earmarked for survey. In addition, all schools, health facilities, religious centres were observed for the presence of functional latrines, handwashing facilities and evidence of open defecation. Open areas, such as sport fields, and previous open defecation sites were observed for evidence of faeces. The presence of functional community latrines was also assessed. <br>

              <br>
      Of the 3286 households visited during ODF evaluation, 3161 (96%) had functional latrines. 67% of the households without latrines fell within three kebeles (Basa Gofara, Dachi Gofara, Gara Goda and Tiyo Hembecho). Reporting latrine use in households without latrines, some of these households admitted to defecating in the surrounding bushes or burying their faeces. All health facilities had latrines that were functional. However, six out of seven religious centres, and three out of 19 schools did not have latrines. There was evidence of both fresh and dry faeces in public areas, mostly by the road side, with Gara Goda and Admancho Arfita kebeles recording the most episodes. <br>
