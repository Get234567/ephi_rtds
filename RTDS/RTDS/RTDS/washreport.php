                MDA in Bolosso Sore was carried out between November 20th and December 13th, 2019. Overall, treatment coverage was 63% for both albendazole (ALB) and praziquantel (PZQ).
                The total population of Bolosso Sore was determined by the Geshiyaro census (as per the census report circulated in March 2020, which included all individuals registered in 2018 and the “mop-up” in 2019). All individuals in the population denominator included those ≥ 1 year who are eligible for ALB and those ≥ 4 years for PZQ. Women who presented at MDA but declined treatment citing the reason as ‘pregnant’

      <br>
                                A total of 132,966 treatment records were registered in the November Bolosso Sore MDA. Data removed included <br> (i) duplicated individual records and <br> (ii) fraudulent names. A total of 23,315 treatment records were removed in the cleaning process, resulting in 109,652 records in the clean MDA treatment dataset (including both ALB and/or PZQ treatment).
                          i - Duplicated Records (Identification Pathways)
                          A total of 112,924 treatment records were registered through one of the three identification pathways (biometric, ID card or name search). Overall, 19,285 treatment records were discarded due to duplication. Reasons for duplication included multiple records with the same ID card, biometric, searched name or a combination of the pathways (i.e. one treatment record by biometric identification, in addition to a separate treatment record documented by ID card identification for the same individual).
                          For example, one subject (barcode: 123123, kebele: Tiyo Hembecho) had 315 treatment records that had the same study ID card. As per protocol, if a biometric identification is not successful the HEW is prompted to attempt a biometric registration for that individual. For 59 of these records a new biometric registration occurred in the treatment record. It is not possible to determine from the data whether the HEW is purposefully duplicating data or does not have the skills to follow the form instructions.
                          It is important to note that the majority (77.4%) of duplicated treatment records were found to be in a pair (i.e. 2 records to one participant), which implies a higher degree of certainty that this is a true duplicate and the same person and not fraudulent registration. However, these extreme ‘duplication’ examples do exist and require an extensive amount of work in cleaning the data to determine the true individual.
                          ii - New Registration Treatment Records
                          17,126 treatment records were received through the ‘new registration’ pathway. For all new registration records, each person was assessed to determine: <br>(i) if the name was a true name (i.e. not xyz or 123),<br> (ii) if the name string was a ‘full’ name string (e.g. ‘John Smith’), and <br>(iii) if there was a census record that matches the demographic information of the new registration in the kebele. All treatment records where the name string was deemed to be a false name were removed. 3,376 treatment records were removed due to a ‘false’ name string. The remaining 13,750 records (17,126-3,376 = 13,750) were verified that they did not match another treatment record (this resulted in the removal of 654 records).

